class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int n)
    {
        // code here.. 
        stack<int>temp;
        int cnt = n/2;
        while(cnt!=0)
        {
            int res = s.top();
            s.pop();
            temp.push(res);
            cnt--;
        }
        s.pop();
        while(temp.empty()==false)
        {
            int res1 = temp.top();
            temp.pop();
            s.push(res1);
        }
        
    }
};

